<?php
function sum($num1, $num2, $num3) {
    $total = $num1 + $num2 + $num3;
    return $total;
}

// Call the function with three arguments and store the result in a variable
$result = sum(5, 10, 15);

// Print the result
echo "The sum of the numbers is: $result"; // Output: The sum of the numbers is: 30

?>